my_String = "Hello World"

# indexing
print(my_String[8])

#  finding the last character in string
print(my_String[-1])

# slicing - getting subsection of a string
myString = 'abcdefghijk'

print(myString[3:6])

# step size
print(myString[::2])

# combination
print(myString[2:7:2])

# backward step
print(myString[::-1])